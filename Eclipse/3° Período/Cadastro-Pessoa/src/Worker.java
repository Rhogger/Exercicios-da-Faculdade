
public class Worker extends Person{
	
	private String office;
	private Double salary;
	
	public String getFuncao() {
		return office;
	}
	public void setFuncao(String funcao) {
		this.office = funcao;
	}
	public Double getSalario() {
		return salary;
	}
	public void setSalario(Double salario) {
		this.salary = salario;
	}
	
}
